use osp;

CREATE TABLE 회원정보 (
    회원ID INT PRIMARY KEY,
    이름 VARCHAR(50),
    이메일 VARCHAR(100),
    비밀번호 VARCHAR(100),
    주소 VARCHAR(200),
    전화번호 VARCHAR(20),
    생년월일 DATE,
    가입일자 DATE
);

CREATE TABLE 상품 (
    상품ID INT PRIMARY KEY,
    이름 VARCHAR(100),
    설명 TEXT,
    가격 DECIMAL(10, 2),
    재고수량 INT,
    판매옵션 VARCHAR(100),
    판매수량 INT,
    브랜드 VARCHAR(50)
);

CREATE TABLE 주문정보 (
    주문ID INT PRIMARY KEY,
    사용자ID INT,
    주문일자 DATE,
    요청사항 TEXT,
    수령인 VARCHAR(50),
    주문총액 DECIMAL(10, 2),
    배송지 VARCHAR(200),
    주문상태 VARCHAR(20),
    FOREIGN KEY (사용자ID) REFERENCES 회원정보(회원ID)
);

CREATE TABLE 쿠폰 (
    쿠폰ID INT PRIMARY KEY,
    쿠폰명 VARCHAR(100),
    할인율 DECIMAL(5, 2),
    쿠폰기간 VARCHAR(50),
    할인설명 TEXT,
    최대할인금액 DECIMAL(10, 2)
);

CREATE TABLE 장바구니 (
    장바구니ID INT PRIMARY KEY,
    사용자ID INT,
    등록일자 DATE,
    등록수량 INT,
    장바구니총액 DECIMAL(10, 2),
    FOREIGN KEY (사용자ID) REFERENCES 회원정보(회원ID)
);

CREATE TABLE 결제정보 (
    결제ID INT PRIMARY KEY,
    주문ID INT,
    결제일자 DATE,
    결제금액 DECIMAL(10, 2),
    결제수단 VARCHAR(20),
    FOREIGN KEY (주문ID) REFERENCES 주문정보(주문ID)
);

CREATE TABLE 배송정보 (
    배송ID INT PRIMARY KEY,
    주문ID INT,
    배송주소 VARCHAR(200),
    배송상태 VARCHAR(20),
    배송일자 DATE,
    예상도착일자 DATE,
    FOREIGN KEY (주문ID) REFERENCES 주문정보(주문ID)
);

CREATE TABLE 찜 (
    찜ID INT PRIMARY KEY,
    사용자ID INT,
    찜한일자 DATE,
    FOREIGN KEY (사용자ID) REFERENCES 회원정보(회원ID)
);

CREATE TABLE 주문상품 (
    주문ID INT,
    상품ID INT,
    주문수량 INT,
    PRIMARY KEY (주문ID, 상품ID),
    FOREIGN KEY (주문ID) REFERENCES 주문정보(주문ID),
    FOREIGN KEY (상품ID) REFERENCES 상품(상품ID)
);

